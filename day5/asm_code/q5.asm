;multiplication of series

MVI B,01H;
LXI H,2000H;

MVI D,05H;
MOV E,D;

LOOP:CALL MULTIPLY
INR B
DCR D
JNZ LOOP

LXI H,2000H;
MVI A,00H;
CALL FINAL_ADD

HLT

FINAL_ADD:ADD M
INX H
DCR E;
JNZ FINAL_ADD 
STA 4000H;
RET

MULTIPLY:MOV C,B
INR C
CALL MUL
INX H
MOV M,A
MVI A,00H
RET



MUL:ADD B;
DCR C
JNZ MUL;


RET

